# integer
integer = 100
print(integer)

# print the type of variable
print(type(integer))

# boolean
boolean = True
print(boolean)

# float
float = 3.14
print(float)

# imaginary number
imaginary = 10j
print(imaginary)

# string
text = "hello bob"
print(text)

# list, sequence
numbers = [1, 2, 3]
print(numbers)

# sequence
coordinates = (2.5, 10)
print(coordinates)

# mapping types
users = {'mario': 1, 'luigi': 2}
print(users)

# set types
raffle = {1, 10, 25, 50}
print(raffle)
